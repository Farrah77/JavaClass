package com.class1;

public class VariablesDeclaration {

	public static void main(String[] args) {

		// different ways to create variable;
		// 1. create vaiable and assign value
		int num = 28;

		// 2 option: using 2 steps
		int y; // 1. declare variable
		y = 12; // 2. assign value to variable

		// declare 1 by 1
		int a;
		int b;
		int c;

		// or declare all together
		int x, s, d;

		a = 10;
		b = 20;

	}
}
